function addTwoNumbers(num1, num2) {
  return num1 + num2;
}

const sum = addTwoNumbers(3, 4);
console.log(sum);

function convertHoursToMinutes(hours) {
  return hours * 60;
}

const minutes = convertHoursToMinutes(4);
console.log(minutes);

function getGreeting(name) {
  return 'Hello, ' + name + '!';
}

const greeting = getGreeting('World');
console.log(greeting);

function addAndMultiplyBy5(num1, num2) {
  return (num1 + num2) * 5;
}

const sumTimesFive = addAndMultiplyBy5(2, 4);
console.log(sumTimesFive);

function multiplyAndDivideBy5(num1, num2) {
  return (num1 * num2) / 5;
}

const productDividedByFive = multiplyAndDivideBy5(7, 10);
console.log(productDividedByFive);

function subtractTwoNumbers(num1, num2) {
  return num1 - num2;
}

const difference = subtractTwoNumbers(8, 3);
console.log(difference);

function getCircleCircumference(radius) {
  return 2 * 3.14 * radius;
}

const circumference = getCircleCircumference(4);
console.log(circumference);

function getFullName(firstName, lastName) {
  return firstName + ' ' + lastName;
}

const fullName = getFullName('Ahmad', 'El-Mowafy');
console.log(fullName);

function cube(number) {
  return number * number * number;
}

const cubed = cube(9);
console.log(cubed);
